'''
Pedir 2 numeros todas las operaciones basicas
'''

numA = int(input("Dame un numero para A: "))
numB = int(input("Dame un numero para B: "))

print(f'Las suma de {numA} y {numB} es: {numA+numB}')
print(f'Las resta de {numA} y {numB} es: {numA-numB}')
print(f'Las multi de {numA} y {numB} es: {numA*numB}')
print(f'Las div de {numA} y {numB} es: {numA/+numB}')
